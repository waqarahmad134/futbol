export function getDailyGuessPlayer(): GuessPlayer {
  const epoch = new Date(2024, 0, 1).getTime();
  const now = new Date();
  const today = new Date(now.getFullYear(), now.getMonth(), now.getDate()).getTime();
  const dayIndex = Math.floor((today - epoch) / 86400000);
  return GUESS_PLAYERS[dayIndex % GUESS_PLAYERS.length];
}

// Progressive clues - revealed one at a time
export function getClues(player: GuessPlayer): string[] {
  return [
    `Position: ${player.position}`,
    `Nationality: ${player.nationality}`,
    `Shirt Number: #${player.shirtNumber}`,
    `Achievement: ${player.achievements[0]}`,
    player.previousClubs.length > 0
      ? `Former Club: ${player.previousClubs[player.previousClubs.length - 1]}`
      : `Fun Fact: ${player.funFact}`,
    `Current/Last Club: ${player.currentClub}`,
  ];
}
